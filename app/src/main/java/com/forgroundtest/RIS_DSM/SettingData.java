package com.forgroundtest.RIS_DSM;

public class SettingData {
    private int icon;
    private String menu;

    public SettingData(int icon, String menu) {
        this.icon = icon;
        this.menu = menu;
    }

    public int getIcon() {
        return icon;
    }

    public String getMenu() {
        return menu;
    }
}
